CREATE TABLE T_SPRING_BOARD_ADVANCE (
	NUM  		NUMBER PRIMARY KEY,
	WRITER 		VARCHAR2(50),
	EMAIL 		VARCHAR2(50),
	SUBJECT 	VARCHAR2(50),
	PASSWORD 	VARCHAR2(20),
	REG_DATE 	DATE,
	REF 		NUMBER,             
	RE_STEP 	NUMBER,     	  
	RE_LEVEL 	NUMBER,		  
	READ_COUNT 	NUMBER,
	CONTENT 	VARCHAR2(1000)
);

SELECT
		*
FROM
		T_SPRING_BOARD_ADVANCE;